package com.nedap.go.game;

/**
 * Represents the position on the board where a stone can be placed.
 */

public class Position {
    private int row;
    private int column;

    public Position(int row, int column) {
        this.row = row;
        this.column = column;
    }

    public int getRow() {
        return row;
    }

    public int getColumn() {
        return column;
    }
}
